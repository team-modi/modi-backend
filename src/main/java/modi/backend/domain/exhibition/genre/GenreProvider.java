package modi.backend.domain.exhibition.genre;

/**
 * 장르 분류 결과의 출처(정준층 계보).
 *
 * <p>벤더가 아니라 <b>출처의 종류</b>를 구분한다 — 벤더가 늘어도 값만 추가되고 스키마·읽기 경로는 불변이다.
 * (감상문 AI가 Gemini → Claude로 실제 전환된 전례가 있어 가설이 아니다.)
 *
 * <p>이 값이 없으면 랜덤 폴백값과 AI 분류값이 구분되지 않아, 폴백이 저장되는 순간 미분류 대상에서 빠져
 * 영구 이탈한다. {@link #RANDOM}을 남기는 것이 그 이탈을 되돌릴 수 있게 하는 장치다.
 */
public enum GenreProvider {

	/** Gemini가 분류. */
	GEMINI,

	/** Claude가 분류(2차 폴백 체인 포함 — ADR-11). */
	CLAUDE,

	/**
	 * (레거시 — 신규 생산자 없음, ADR-11에서 제거) 분류기 미설정·한도 초과·오류로 랜덤 폴백된 값.
	 * 기존 행 판독을 위해 enum 값만 남긴다. 실패는 이제 폴백값이 아니라 예외 → 아웃박스 재시도로 표현된다.
	 */
	RANDOM,

	/** 로컬/CI/키없음 환경의 결정적 분류기(제목 기반) — AI 호출·비용 0. 실 분류가 아니므로 운영 판독 시 구분된다. */
	MOCK,

	/** 사용자가 CUSTOM 등록 시 직접 지정. */
	USER,

	/**
	 * 이관(V21) 전에 이미 {@code exhibitions.genre_keyword}에 채워져 있던 값 — 출처 정보가 DB에 물리적으로 없어
	 * "모른다"를 정직하게 남긴다.
	 *
	 * <p>왜 다른 값으로 적지 않았나: {@link #GEMINI}로 적으면 랜덤 폴백분까지 AI 분류로 둔갑해 이 enum이 풀려던
	 * 영구 이탈이 그대로 봉인되고, {@link #RANDOM}으로 적으면 진짜 AI 분류분까지 재분류 대상이 되어 호출을 낭비한다.
	 * 둘 다 <b>없는 사실을 지어내는 것</b>이다. 이 값은 "재분류할지"를 나중에 데이터를 보고 정하게 남긴다.
	 *
	 * <p>⚠️ "분류 실패"로 읽지 마라 — 분류는 됐고, 누가 했는지의 기록이 없을 뿐이다.
	 * 코드가 이 값을 만드는 경로는 없다(V21 백필이 유일한 생산자다).
	 */
	UNKNOWN
}
